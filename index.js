// server/routes.ts
import { z } from "zod";
async function registerRoutes(app2) {
  app2.post("/api/inquiries", async (req, res) => {
    try {
      const validatedData = insertInquirySchema.parse(req.body);
      const inquiry = await storage.createInquiry(validatedData);
      res.status(201).json({
        message: "Inquiry submitted successfully",
        inquiry
      });
    } catch (error) {
      if (error instanceof z.ZodError) {
        return res.status(400).json({
          message: "Invalid inquiry data",
          errors: error.errors
        });
      }
      console.error("Error creating inquiry:", error);
      res.status(500).json({
        message: "An error occurred while submitting your inquiry"
      });
    }
  });
  app2.get("/api/inquiries", async (req, res) => {
    try {
      const inquiries2 = await storage.getInquiries();
      res.json(inquiries2);
    } catch (error) {
      console.error("Error fetching inquiries:", error);
      res.status(500).json({
        message: "An error occurred while fetching inquiries"
      });
    }
  });
  const httpServer = createServer(app2);
  return httpServer;
}

